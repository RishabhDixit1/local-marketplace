const MAGIC_BYTES: Record<string, Uint8Array[]> = {
  "image/jpeg": [new Uint8Array([0xFF, 0xD8, 0xFF])],
  "image/png": [new Uint8Array([0x89, 0x50, 0x4E, 0x47])],
  "image/webp": [new Uint8Array([0x52, 0x49, 0x46, 0x46])],
  "image/gif": [new Uint8Array([0x47, 0x49, 0x46, 0x38])],
  "image/heic": [new Uint8Array([0x00, 0x00, 0x00, 0x18, 0x66, 0x74, 0x79, 0x70, 0x68, 0x65, 0x69, 0x63])],
  "image/heif": [new Uint8Array([0x00, 0x00, 0x00, 0x18, 0x66, 0x74, 0x79, 0x70, 0x6D, 0x69, 0x66, 0x33])],
  "video/mp4": [new Uint8Array([0x00, 0x00, 0x00, 0x18, 0x66, 0x74, 0x79, 0x70])],
  "video/webm": [new Uint8Array([0x1A, 0x45, 0xDF, 0xA3])],
  "video/quicktime": [new Uint8Array([0x00, 0x00, 0x00, 0x14, 0x66, 0x74, 0x79, 0x70, 0x71, 0x74, 0x20, 0x20])],
  "audio/mpeg": [new Uint8Array([0xFF, 0xFB]), new Uint8Array([0xFF, 0xF3]), new Uint8Array([0xFF, 0xF2])],
  "audio/wav": [new Uint8Array([0x52, 0x49, 0x46, 0x46])],
  "audio/ogg": [new Uint8Array([0x4F, 0x67, 0x67, 0x53])],
  "application/pdf": [new Uint8Array([0x25, 0x50, 0x44, 0x46])],
};

export function validateFileMagicBytes(buffer: Uint8Array, mimeType: string): boolean {
  const signatures = MAGIC_BYTES[mimeType];
  if (!signatures) return true;
  return signatures.some((sig) => {
    if (buffer.length < sig.length) return false;
    return sig.every((byte, i) => buffer[i] === byte);
  });
}
